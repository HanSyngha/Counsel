/// Personalized "Seek Wisdom" button texts for each persona
/// These texts replace the generic "Seek Wisdom" button with persona-specific calls to action
/// Supports 21 languages
///
/// IMPORTANT PRINCIPLE:
/// Button texts must maintain the core meaning of "seeking advice/guidance/wisdom"
/// NOT just character catchphrases or slogans
///
/// Allowed patterns:
/// - Direct question: "Ask the ~", "Inquire of ~"
/// - Request teaching: "Seek ~ teaching", "Request ~'s wisdom"
/// - Learn from experience: "Learn from ~'s experience"
/// - Polite/honorific: "Consult with ~", "Humbly ask ~"
class SeekWisdomButtons {
  SeekWisdomButtons._();

  /// Get list of button texts for a persona in a specific language
  static List<String> getButtonTexts(String personaId, {String languageCode = 'en'}) {
    final langButtons = _buttons[languageCode] ?? _buttons['en']!;
    return langButtons[personaId] ?? [];
  }

  /// Get random button text for a persona, or null if no custom text exists
  static String? getRandomButtonText(String personaId, {String languageCode = 'en'}) {
    final texts = getButtonTexts(personaId, languageCode: languageCode);
    if (texts.isEmpty) return null;
    final shuffled = List<String>.from(texts);
    shuffled.shuffle();
    return shuffled.first;
  }

  /// Check if a persona has custom button texts
  static bool hasCustomButtonText(String personaId, {String languageCode = 'en'}) {
    final langButtons = _buttons[languageCode] ?? _buttons['en']!;
    return langButtons.containsKey(personaId) && langButtons[personaId]!.isNotEmpty;
  }

